CREATE USER almacen IDENTIFIED BY "Herrera2024_" DEFAULT TABLESPACE DATA QUOTA 50M ON DATA ;
GRANT CONNECT, RESOURCE, CREATE VIEW TO almacen;

CREATE TABLE almacen.tipos_pieza(
  tipo          CHAR(2)         CONSTRAINT tipos_pieza_pk PRIMARY KEY,
  descripcion   VARCHAR2(25)    CONSTRAINT tipos_pieza_nn1 NOT NULL
);

CREATE TABLE almacen.piezas(
    tipo            CHAR(2)      CONSTRAINT piezas_fk REFERENCES almacen.tipos_pieza,
    modelo          NUMBER(2),
    precio_venta    NUMBER(11,4) CONSTRAINT piezas_nn1 NOT NULL,
    CONSTRAINT piezas_pk PRIMARY KEY (tipo,modelo)
);    


CREATE TABLE almacen.almacenes(
  n_almacen     NUMBER(2)       CONSTRAINT almacenes_pk PRIMARY KEY,
  descripcion   VARCHAR2(1000)  CONSTRAINT almacenes_nn1 NOT NULL,
  direccion     VARCHAR2(100)
);  
CREATE TABLE almacen.existencias(
    tipo        CHAR(2),
    modelo      NUMBER(2),
    n_almacen   NUMBER(2) CONSTRAINT existencias_fk2 REFERENCES almacen.almacenes,
    cantidad    NUMBER(9) CONSTRAINT existencias_nn1 NOT NULL
    CONSTRAINT existencias_ck1 CHECK(cantidad>0),
    CONSTRAINT existencias_fk1 FOREIGN KEY (tipo,modelo) REFERENCES almacen.piezas(tipo,modelo),
    CONSTRAINT existencias_pk PRIMARY KEY (tipo,modelo,n_almacen)
);

CREATE TABLE almacen.empresas(
  cif       CHAR(9)         CONSTRAINT empresas_pk PRIMARY KEY,
  nombre    VARCHAR2(50)    CONSTRAINT empresas_uk UNIQUE
                            CONSTRAINT empresas_nn NOT NULL,
  telefono  CHAR(9),
  direccion VARCHAR2(50),
  localidad VARCHAR2(50),
  provincia VARCHAR2(30)
);

CREATE TABLE almacen.suministros(
    tipo            CHAR(2),
    modelo          NUMBER(2),
    cif             CHAR(9)     CONSTRAINT suministros_fk2 REFERENCES almacen.empresas,
    precio_compra   NUMBER(11,4),
    CONSTRAINT suministros_fk1 FOREIGN KEY (tipo,modelo) REFERENCES almacen.piezas(tipo,modelo),
    CONSTRAINT suministros_pk PRIMARY KEY (tipo,modelo,cif)
);


CREATE TABLE almacen.pedidos(
    n_pedido    NUMBER(4)   CONSTRAINT pedidos_pk PRIMARY KEY,
    cif         CHAR(9)     CONSTRAINT pedidos_fk REFERENCES almacen.empresas 
                            CONSTRAINT pedidos_nn1 NOT NULL,
    fecha       DATE        CONSTRAINT pedidos_nn2 NOT NULL
);

CREATE TABLE almacen.lineas_pedido(
    n_pedido    NUMBER(4)   CONSTRAINT lineas_pedido_fk1 REFERENCES almacen.pedidos,
    n_linea     NUMBER(2),
    tipo        CHAR(2)     CONSTRAINT lineas_pedido_nn1 NOT NULL,
    modelo      NUMBER(2)   CONSTRAINT lineas_pedido_nn2 NOT NULL,
    cantidad    NUMBER(5),
    precio      NUMBER(11,4),
    CONSTRAINT lineas_pedido_fk2 FOREIGN KEY (tipo,modelo) REFERENCES almacen.piezas(tipo,modelo),
    CONSTRAINT lineas_pedido_pk PRIMARY KEY (n_pedido,n_linea)
);

