SELECT a.NOMBRE, a.APELLIDO1, a.APELLIDO2, tc.TITULO
FROM ALUMNOS a
JOIN ASISTIR S USING(NIF)
JOIN CURSOS c USING (N_CURSO)
JOIN TIPOS_CURSO tc USING (cod_curso)
ORDER BY 2,3,1,4;

