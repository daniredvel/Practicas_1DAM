SELECT l.NOMBRE AS LOCALIDAD, p.NOMBRE AS PROVINCIA, c.NOMBRE AS NOMBRE, l2.NOMBRE AS CAPITAL
FROM LOCALIDADES l
JOIN PROVINCIAS p USING (N_PROVINCIA)
JOIN COMUNIDADES c USING (ID_COMUNIDAD)
JOIN LOCALIDADES l2 USING (ID_LOCALIDAD);
